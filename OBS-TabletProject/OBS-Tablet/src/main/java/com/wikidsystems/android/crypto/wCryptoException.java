package com.wikidsystems.android.crypto;

public class wCryptoException extends Exception {

	public wCryptoException() {
		super();
	}

	public wCryptoException(String arg0) {
		super(arg0);
	}

	public wCryptoException(Throwable arg0) {
		super(arg0);
	}

	public wCryptoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}